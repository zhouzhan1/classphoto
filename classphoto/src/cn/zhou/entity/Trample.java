package cn.zhou.entity;

public class Trample {
	
	private int trampleId;
	private int photoId;
	private int userId;
	
	
	public int getTrampleId() {
		return trampleId;
	}
	public void setTrampleId(int trampleId) {
		this.trampleId = trampleId;
	}
	public int getPhotoId() {
		return photoId;
	}
	public void setPhotoId(int photoId) {
		this.photoId = photoId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}

}
